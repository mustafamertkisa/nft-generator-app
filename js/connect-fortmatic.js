// nft oluşturulması için kontrat ile etkileşim kurulması
async function getNFT() {
    cardName.innerHTML = nftTitle.value;
    cardSymbol.innerHTML = nftSymbol.value;
    document.getElementById("cardImage").src = nftURL.value;

    const MyContract = new web3.eth.Contract(contractAbi, contractAddress);
    let result = await MyContract.methods.createNFT(nftTitle.value, nftSymbol.value, nftURL.value).send({ type: "0x2", from: account }, function (err, resp) {

        if (!err) {
            transactionID = resp;
            document.getElementById("showContractAddress").innerHTML = contractAddress;
            document.getElementById("showTransactionID").innerHTML = transactionID;

        } else {
            console.log(err);
        }
    });
    console.log(result);
}

async function viewNFT() {
	if (contractNetwork == "rinkeby") {
		document.getElementById("viewButton").href = "https://rinkeby.etherscan.io//tx/" + transactionID;
	} else if (contractNetwork == "ropsten") {
		document.getElementById("viewButton").href = "https://ropsten.etherscan.io//tx/" + transactionID;
	} else if (contractNetwork == "kovan") {
        document.getElementById("viewButton").href = "https://kovan.etherscan.io//tx/" + transactionID;
    }
}